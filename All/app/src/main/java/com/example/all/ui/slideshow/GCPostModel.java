package com.example.all.ui.slideshow;

public class GCPostModel {
    String From,To;

    public GCPostModel(String from, String to) {
        From = from;
        To = to;
    }

    public String getFrom() {
        return From;
    }

    public void setFrom(String from) {
        From = from;
    }

    public String getTo() {
        return To;
    }

    public void setTo(String to) {
        To = to;
    }
}
